package JavaBasic;

public class BooleanDataType {
    public static void main(String[] args) {
        // Boolean => 1 or 0, yes or no, true or false, on or off
        // Only 2
        // Default value is 0 or false

        boolean benar = true;
        boolean salah = false;

        System.out.println("benar\t: " + benar);
        System.out.println("salah\t: " + salah);
    }
}
